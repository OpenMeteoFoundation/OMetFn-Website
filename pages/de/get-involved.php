<h1>Mach mit</h1>

<p>Unsere Projekte sind von offenem, freiem Charakter geprägt und wir möchten dich ermuntern, mitzumachen.</p>
<p>Aus welchem Bereich auch immer du kommst, unsere Projekte sind oft fachübergreifend und damit sind alle deine Fähigkeiten nützlich.</p>

<p>Jeder kann seinen Beitrag zur Wissenschaft leisten. Wir sollten Inspiration durch vergangene Projekte wie <a href="http://setiathome.berkeley.edu/" target="_blank">SETI@Home</a> (Wo der Bildschirmschoner nach außerirdischem Leben suchte) oder <a href="http://fold.it" target="_blank">fold.it</a>, (wo <a href="http://www.huffingtonpost.com/2011/09/19/aids-protein-decoded-gamers_n_970113.html" target="_blank">Personen aus der Gamer-Szene ein Problem lösten</a>) erlangen.</p>

<h2>An einem existierenden Projekt teilnehmen</h2>

<p>Du findest auf jeder Projektseite ausführliche Informationen darüber, wie du teilnehmen kannst.</p>

<p>Eine Liste unserer Aktivitäten kannst du auf der <a href="/projects">Projektseite</a> abrufen.</p>

<p>Wir arbeiten hart daran, dir das Teilnehmen zu erleichtern. Nichtsdestotrotz ist die Open Meteo Foundation sehr jung und hier und da reichte die Zeit noch nicht aus, um alle Wege zur Teilnahme zu beschreiben. Bitte zöger deshalb nicht, <a href="/contact">uns zu kontaktieren</a>.</p>

<h2 id="project-proposal">Starte dein eigenes Projekt</h2>

<p>Betreibst du ein interessantes Projekt oder hast du abenteuerliche Idee, die du erforschen möchtest?</p>

<p>Wir können dir bei der Entwicklung deines Projektes helfen, indem wir dir Rechenleistung, Hosting und Archivierung bereitstellen. Du wirst auch von unserem sozialen Netzwerk profitieren.</p>

<p>Wir akzeptieren Projekte, sofern sie folgenden Grundsätzen folgen:</p>
<ul>
  <li>Freie Daten und Inhalte: Daten und Inhalte, auch alle anderen Ressourcen, die vom Projekt erzeugt werden, werden mit einer freiheitlichen Lizenz, die dem Charakter "frei/offen" gerecht wird, versehen.</li>
  <li>Freie/Open-Source-Software: Software, die vom Projekt stammt, wird freiheitlich lizensiert (z.B. in Übereinstimmung mit der OSI Open Source Definition)</li>
  <li>Öffentlichkeit und Höflichkeit: Das Projekt wird auf öffentliche Art und Weise geführt und ermutigt Außenstehende zur Teilnahme. Die Interaktion mit dem Projekt sowie dessen Community gründet sich auf den fundamentalen Prinzipien von Toleranz, Respekt und Anstand.</li>
</ul>

<p>Um dein Projekt mit uns zu besprechen, sende eine E-Mail an getinvolved[a]ometfn.org .</p>

<h2 id="share-your-story">Veröffentliche deine Geschichte</h2>
<p>Hast du eine interessante Geschichte mit Bezug zur Meteorologie oder dem Gedanken freiheitlicher Projekte?</p>
<p>Hattest du je Schwierigkeiten, an Wetterdaten zu gelangen?</p>

<p>Wir möchten dich ermutigen, deine Geschichte mit der ganzen Welt zu teilen. Sende uns einfach eine E-Mail an blog[a]ometfn.org. Wir wären erfreut, deinen Bericht in <a href="http://blog.openmeteofoundation.org" target="_blank">unserem Blog</a> veröffentlichen zu können.</p>
