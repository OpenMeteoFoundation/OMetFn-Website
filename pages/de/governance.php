<h1>Projektleitung</h1>
<h2>Über die Open Meteo Foundation</h2>
<p>Unsere <a title="About us" href="/about-us">Über uns</a>-Seite bietet einen Überblick über OMetFn, unsere Vision, wie unsere Community funktioniert und informiert über organisatorische Fakten.</p>
<h2>Details zur Projektleitung</h2>
<p>Wir streben Projekte an, die von Community-Gruppen betreut werden und durch das OMetFn-Team Unterstützung erhalten.</p>
<p><a title="Vorstand" href="/board/#nicolas">Nicolas BALDECK</a> (Gründer und Vorsitzender) verwaltet OMetFn federführend in Fragen des allgemeinen Managements, der strategischen Ausrichtung und berichtet dem <a title="Board of Directors" href="/board">Vorstand</a>, welcher für die finanzielle und juristische Rechtmäßigkeit der Open Meteo Foundation verantwortlich ist.</p>
<p>Ein separates Beratungsgremium zur Unterstützung und Begleitung durch Experten befindet sich in Planung.</p>
