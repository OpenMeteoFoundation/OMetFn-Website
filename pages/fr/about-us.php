<h1>À propos de l'Open Meteo Foundation</h1>

<p>L'Open Meteo Foundation (OMetFn) est une association à but non lucratif fondée en 2012 et dédiée à la promotion de l'open data, l'open knowledge, l'open technology et l'open science appliqués à la météorologie.</p>

<p>Né d'une initiative personnelle <a title="Opening the Weather" href="http://blog.openmeteofoundation.org/2013/07/opening-weather.html" target="_blank">visant à obtenir de meilleures prévisions météo pour le parapente</a>, le projet a pri de l'ampleur quand nous avons réalisé qu'il y avait de grands enjeux à la clé.</p>

<p>Nous avons bénéficié d'un grand soutien de la communauté du Vol Libre et de <a title="Nos partenaires" href="/partners">nos partenaires</a>. Cela nous a permis de construire les bases d'un projet solide et crédible. Maintenant, nous passons à l'action.</p>


<h2>Notre vision</h2>
<p>Depuis des siècles, les institutions publiques et privées ont fait un remarquable travail pour mieux comprendre et tirer profit de la météo. Cependant, il reste encore beaucoup à faire.</p>
<p>Nous pensons qu'en faisant travailler ensemble les universitaires, les geeks et les citoyens, nous allons mettre en avant la météo dans la société. L'innovation sera stimulée par la croisée de nos compétences, ce qui pourra améliorer notre mode de vie. La météo est la clé d'une société plus sûre et plus durable.</p>

<p>Nous savons que des amateurs regroupés autour d'une idée peuvent réaliser de grandes choses, que des institutions publiques ou privées n'auraient jamais pu accomplir. Les projets communautaires comme Linux, Wikipédia ou OpenStreetMap nous ont montré le chemin...</p>

<h2>Activités</h2>
<p>L'Open Meteo Foundation a pour vocation de soutenir la communauté mondiale de la météo « ouverte ».
Nous voulons être un incubateur pour des projets ouverts de tout type, gravitants autour de la météo.</p>
<p>L'activité historique de l'OMetFn est de produire des prévisions à haute résolution pour la France et les Alpes.</p>
<p>À partir de là, nous voulons avancer vers de nouveaux projets, tels que :</p>
<ul>
<li>faire des prévisions ouvertes pour le monde entier</li>
<li>faciliter l'accès aux données existantes</li>
<li>écrire un manuel des données météo pour les nuls</li>
<li>monter un réseau mondial de stations amateures</li>
<li>catalyser le débat politique sur l'ouverture des données publiques</li>
</ul>
