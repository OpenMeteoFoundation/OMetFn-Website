<h3 id="why">Pourquoi nous soutenir ?</h3>
<p>L'Open Meteo Foundation est une association à but non lucratif. Notre travail est financée par les dons généreux de particuliers et d'entreprises.</p>
<p>Nous faisons la promotion de l'open data, l'open knowledge, l'open technology et l'open science appliqués à la météorologie. <strong>Cela demande du temps et de l'argent :</strong></p>
<p>La plupart des coûts proviennent de notre infrastructure informatique :<br />
Nous avons des serveurs web, notre propre cluster HPC pour calculer les prévisions, nous archivons les données. Nous avons besoin d'aide pour financer le matériel ainsi que l'électricité et la bande passante.</p>
<p>Pour fabriquer de l'open hardware, il nous faut acheter du matériel, réaliser des prototypes…</p>
<p>Nous avons aussi besoin de nous déplacer pour donner des conférences et participer à des réunions afin de répendre nos idées.</p>
<p><strong>Pour ces raisons, votre soutien est crucial pour nous permettre de continuer et d'étendre notre travail.</strong></p>
<p>Si vous voulez en savoir plus sur nos activités, vous pouvez jeter un œuil à notre page <a href="/projects" title="Projects">projets</a>. Vous trouverez nos derniers rapports d'activités et financiers sur notre page <a href="/governance" title="Governance" target="_blank">gestion</a>. </p>

<h3>Les autres manières de soutenir l'Open Meteo Foundation</h3>
<p>Vous pouvez nous soutenir de bien d'autres façons que par l'argent. La plupart de ce que nous faisons dépend de bénévoles, <strong>qui donnent de leur temps et de leur compétence</strong> — cela en s'impliquant dans la gestion de l'association, en participant à un projet, en nous aidant à améliorer notre site internet, en organisant un évenement et bien plus... <strong>Pourquoi ne pas devenir l'un d'eux ?</strong> Si vous voulez contribuer, <a title="Get involved !" href="/get-involved">c'est par ici</a>.</p>
