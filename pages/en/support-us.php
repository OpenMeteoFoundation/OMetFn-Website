<h3 id="why">Why support us?</h3>
<p>The Open Meteo Foundation is a not-for-profit organization. We rely on generous donation from individuals and companies to support our work.</p>
<p>We are promoting open data, open knowledge and open science in the field of meteorology. <strong>But doing this requires time and money:</strong></p>
<p>Most of our costs comes from our computer infrastructure:<br />
We are setting-up web servers, operating High-Performance-Computing cluster for running forecasts, archiving the data&#8230; We need support for hardware, as well as electricity and bandwidth bill.</p>
<p>We also need to travel for giving talks and attending meetings, spreading our ideas for weather openness.</p>
<p><strong>Your support is therefore crucial to us in continuing and expanding our work.</strong></p>
<p>If you would like to know more about our activities please take a look at our <a href="/projects" title="Projects">projects</a> page. You&#8217;ll find our most recent annual reports and our annual accounts on our <a href="/governance" title="Governance" target="_blank">governance</a> page. </p>
<h3>Other ways to support the Open Meteo Foundation</h3>
<p>You can support the Open Meteo Foundation in many other ways than via a financial contribution. Much of what we do depends on those <strong>who volunteer their time and effort</strong> &#8211; be that acting as a board member, helping to develop and maintain our websites, organizing events, and much more. <strong>Why not become one of them?</strong> If you would like to get involved please visit the <a title="Get involved !" href="/get-involved">Get involved</a> page.</p>
